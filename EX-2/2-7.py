name = "\t Tushar Kirtunia \n"
#The string contains whitespace characters:
	#•	\t → Represents a tab (adds extra space before the text).
	#•	\n → Represents a newline (moves to the next line after the text).

print("Dilip Kirtunia")
print(name)

print("\nusing lstrip():")
print(name.lstrip())

print("\nusing rstrip():")
print(name.rstrip())

print("\nusing strip():")
print(name.strip())